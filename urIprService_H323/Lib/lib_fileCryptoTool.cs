        }

        public static string ForceCreateFolder(string path) {
            var err = "";
            if (Directory.Exists(path))
                return err;
            try {
                Directory.CreateDirectory(path);
            }
            catch (Exception ex) {
                err = ex.Message;
            }
            return err;
        }

        /// <summary>
        /// 嘗試取得可讀取的 FileStream。
        /// 規則：
        /// 1) 檢查檔案是否存在；不存在回傳 null，並填入錯誤訊息。
        /// 2) 若 IsNewEncryptedFormat(filePath) 為 true，先解密到暫存檔，再以 DeleteOnClose 開啟串流。
        /// 3) 非加密則直接開啟原檔案（Read / Share ReadWrite|Delete）。
        /// 4) 任何例外都回傳 null，並在 error 填入原因。
        /// </summary>
        public static FileStream? GetFileStream(string inputFile, out string finalOutput, out bool excrypted, string password, out string error) {
            error = string.Empty;
            finalOutput = inputFile;
            excrypted = false;
            if (string.IsNullOrWhiteSpace(inputFile)) {
                error = "filePath 不能為空白。";
                return null;
            }

            try {
                if (!File.Exists(inputFile)) {
                    error = $"檔案不存在：{inputFile}";
                    return null;
                }

                // 需要解密？
                if (IsNewEncryptedFormat(inputFile)) {
                    excrypted = true;                    
                    if (!DecryptToTempFile(inputFile, out finalOutput, password, out error)) {                        
                        return null;
                    }

                    // 用 DeleteOnClose，確保串流關閉即刪除暫存明文
                    return new FileStream(
                        finalOutput!,
                        new FileStreamOptions {
                            Mode = FileMode.Open,
                            Access = FileAccess.Read,
                            Share = FileShare.ReadWrite | FileShare.Delete,
                            Options = FileOptions.SequentialScan | FileOptions.Asynchronous | FileOptions.DeleteOnClose
                        });
                }
                else {
                    // 非加密，直接開啟原檔案
                    return new FileStream(
                        inputFile,
                        new FileStreamOptions {
                            Mode = FileMode.Open,
                            Access = FileAccess.Read,
                            Share = FileShare.ReadWrite | FileShare.Delete,
                            Options = FileOptions.SequentialScan | FileOptions.Asynchronous
                        });
                }
            }
            catch (UnauthorizedAccessException ex) {
                error = $"存取被拒：{ex.Message}";
                return null;
            }
            catch (IOException ex) {
                // 常見：檔案被占用、I/O 裝置錯誤、路徑或分享模式衝突
                error = $"I/O 錯誤：{ex.Message}";
                return null;
            }
            catch (Exception ex) {
                error = $"未預期的錯誤：{ex.Message}";
                return null;
            }
        }
    }


